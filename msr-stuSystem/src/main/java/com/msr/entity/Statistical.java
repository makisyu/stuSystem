package com.msr.entity;

import org.springframework.stereotype.Component;

@Component
public class Statistical {
	private int maleCount;//男生人数
	private int femaleCount;//女生人数
	private int counts;//总人数
	private String clazzName;//班级名称
	private int yearCounts;//按年份分组统计人数
	private String years;//年份	
	private String companyName;//企业名称
	
	
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public String getYears() {
		return years;
	}
	public void setYears(String years) {
		this.years = years;
	}
	public int getYearCounts() {
		return yearCounts;
	}
	public void setYearCounts(int yearCounts) {
		this.yearCounts = yearCounts;
	}
	public int getMaleCount() {
		return maleCount;
	}
	public void setMaleCount(int maleCount) {
		this.maleCount = maleCount;
	}
	public int getFemaleCount() {
		return femaleCount;
	}
	public void setFemaleCount(int femaleCount) {
		this.femaleCount = femaleCount;
	}
	public int getCounts() {
		return counts;
	}
	public void setCounts(int counts) {
		this.counts = counts;
	}
	public String getClazzName() {
		return clazzName;
	}
	public void setClazzName(String clazzName) {
		this.clazzName = clazzName;
	}
}
